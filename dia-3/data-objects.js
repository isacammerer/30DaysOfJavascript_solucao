//data objects - pegar informações de tempo sobre um evento ou atividade, utilizando o método get


//creating a time object
const now = new Date()
console.log(now)

//full year
console.log(now.getFullYear())

//month
console.log(now.getMonth())         //mese contam de 0-11

//date
console.log(now.getDate())          

//day
console.log(now.getDay())           //dias da semana contam 0-6

//hours
console.log(now.getHours())         

//minutes
console.log(now.getMinutes())       



